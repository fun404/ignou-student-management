				<p>
					Software Engineering also called Software development process. A process used by the software industry to design, develop and test high quality softwares. The SDLC aims to produce a high quality software that meets or exceeds customer expectations, reaches completion within times and cost estimates.
				</p>
				Software Engineering helps to apply theoretical knowledge of Computer Science for building high-quality software products for various applications.
				<p>
					A Software is a set of instructions or data that operates the hardware. Application is a package to perform a specific task. Software is an all encompassing term for computer data. Application is a type of software that does a certain task.
				</p>
				<p>
					Types of Software: <br>

					Console applications are light weight programs run inside the command prompt (DOS) window. They are commonly used for test applications. <br>


					Windows Applications are form based standard Windows desktop applications for common day to day tasks. Microsoft word is an example of a Windows application. <br>


					Web applications are programs that used to run inside some web server (e.g., IIS) to fulfill the user requests over the http. A typical example of web application is Hotmail and Google. <br>


					Web services are web applications that provide services to other applications over the internet. Google search engine's web service, e.g., allows other applications to delegate the task of searching over the internet to Google web service and use the result produced by it in their own applications. <br>
				</p>
